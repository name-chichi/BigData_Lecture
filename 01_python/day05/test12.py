score = [
    ['2019-01-03',10,12,14,10],
    ['2020-02-03',12,12,14,11],
    ['2020-03-03',13,13,15,12],
    ['2020-03-04',14,14,16,13],
    ['2020-04-03',15,15,17,14]
];

for data in score:
    print('%s 온도 평균은 %.2f' % (data[0],sum(data[1:])/len(data[1:])));

print('----------------------------------------------');

ss = 0;
cnt = 0;
for data in score:
    if '2020' in data[0]:
        ss += sum(data[1:]);
        cnt += len(data[1:]);
print(ss/cnt);